        'content'    => array(__('Visible in post', 'botwriter'), 'bw-chip-success'),
        'a11y'       => array(__('Accessibility', 'botwriter'), 'bw-chip-info'),
        'schema'     => array(__('Adds JSON-LD schema', 'botwriter'), 'bw-chip-info'),
        'index'      => array(__('Hidden DB index', 'botwriter'), 'bw-chip-muted'),
        'autopilot'  => array(__('Compatible with Autopilot', 'botwriter'), 'bw-chip-success'),
        'ai'         => array(__('Uses AI quota', 'botwriter'), 'bw-chip-warn'),
        'url'        => array(__('Changes URL', 'botwriter'), 'bw-chip-warn'),
        'redirect'   => array(__('Auto 301 redirect', 'botwriter'), 'bw-chip-info'),
        'destructive'=> array(__('Destructive — confirm required', 'botwriter'), 'bw-chip-danger'),
    );
    $undo_labels = array(
        'meta'    => array(
            __('Simple UNDO: yes', 'botwriter'),
            'bw-chip-success',
            __('Can restore the previous SEO/meta value with a simple before-run snapshot.', 'botwriter')
        ),
        'content' => array(
            __('Simple UNDO: yes', 'botwriter'),
            'bw-chip-success',
            __('Can restore the previous post body with one content snapshot taken before the action runs.', 'botwriter')
        ),
        'media'   => array(
            __('Simple UNDO: yes', 'botwriter'),
            'bw-chip-info',
            __('Can restore the previous ALT values with a lightweight media/meta snapshot.', 'botwriter')
        ),
        'none'    => array(
            __('Simple UNDO: no', 'botwriter'),
            'bw-chip-danger',
            __('This action should stay outside the simple UNDO flow because reverting it cleanly is risky or low-value.', 'botwriter')
        ),
    );
    $undo_key = isset($meta['undo']) && isset($undo_labels[$meta['undo']]) ? $meta['undo'] : 'none';
    list($undo_label, $undo_class, $undo_help) = $undo_labels[$undo_key];

    echo '<div class="bw-card bw-action-card' . ($meta['destructive'] ? ' is-destructive' : '') . '">';
    echo '<details class="bw-action-details">';

    // Compact header (collapsed state).
    echo '<summary class="bw-action-summary">';
    echo '<div class="bw-action-head">';
    echo '<span class="bw-action-icon"><span class="dashicons dashicons-' . esc_attr($meta['icon']) . '"></span></span>';
    echo '<div class="bw-action-titles">';
    echo '<div class="bw-action-title-row">';
    echo '<h4>' . esc_html($meta['label']) . '</h4>';
    echo '<span class="bw-info-chip bw-action-undo-chip ' . esc_attr($undo_class) . '">' . esc_html($undo_label) . '</span>';
