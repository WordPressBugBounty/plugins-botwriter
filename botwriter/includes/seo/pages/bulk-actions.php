/**
 * Render a single bulk-action card with what / where / why / use-cases /
 * badges and the Configure & Run button.
 */
function botwriter_seo_render_action_card($action_key, $meta, $running) {
    $badge_labels = array(
        'search'     => array(__('Affects search snippet', 'botwriter'), 'bw-chip-info'),
