    echo '<p class="bw-action-tagline">' . esc_html($meta['tagline']) . '</p>';
    echo '</div>';
    echo '</div>';
    echo '<span class="bw-action-toggle dashicons dashicons-arrow-down-alt2" aria-hidden="true"></span>';
    echo '</summary>';

    // Expanded body.
    echo '<div class="bw-action-body">';

    if (!empty($meta['badges'])) {
        echo '<div class="bw-action-chips">';
        foreach ($meta['badges'] as $b) {
            if (!isset($badge_labels[$b])) { continue; }
            list($label, $class) = $badge_labels[$b];
            echo '<span class="bw-info-chip ' . esc_attr($class) . '">' . esc_html($label) . '</span>';
        }
        echo '</div>';
    }

    echo '<dl class="bw-action-explain">';
    echo '<dt><span class="dashicons dashicons-admin-tools"></span> ' . esc_html__('What it does', 'botwriter') . '</dt>';
    echo '<dd>' . (!empty($meta['what_html']) ? wp_kses_post($meta['what_html']) : esc_html($meta['what'])) . '</dd>';
    echo '<dt><span class="dashicons dashicons-visibility"></span> ' . esc_html__('Where the result appears', 'botwriter') . '</dt>';
    echo '<dd>' . esc_html($meta['where']) . '</dd>';
    echo '<dt><span class="dashicons dashicons-chart-line"></span> ' . esc_html__('Why it improves SEO', 'botwriter') . '</dt>';
    echo '<dd>' . esc_html($meta['why']) . '</dd>';
    echo '<dt><span class="dashicons dashicons-undo"></span> ' . esc_html__('Undo status', 'botwriter') . '</dt>';
    echo '<dd>' . esc_html($undo_help) . '</dd>';
    echo '</dl>';

    if (!empty($meta['use_cases'])) {
        echo '<div class="bw-action-uses">';
        echo '<strong>' . esc_html__('Best for', 'botwriter') . ':</strong>';
        echo '<ul>';
        foreach ($meta['use_cases'] as $u) {
            echo '<li>' . esc_html($u) . '</li>';
        }
        echo '</ul>';
        echo '</div>';
    }

    echo '<div class="bw-card-actions">';
    echo '<button type="button" class="bw-button primary bw-job-configure"';
    echo ' data-job="actions"';
    echo ' data-mode="action"';
    echo ' data-action-label="' . esc_attr($meta['label']) . '"';
    echo ' data-destructive="' . ($meta['destructive'] ? '1' : '0') . '"';
    echo ' data-features=\'' . esc_attr(wp_json_encode($meta['features'])) . '\'';
    echo ' data-args=\'' . esc_attr(wp_json_encode(array_merge(array('action' => $action_key), isset($meta['args']) && is_array($meta['args']) ? $meta['args'] : array()))) . '\'';
